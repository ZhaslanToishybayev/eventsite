    def get_interview_statistics(self) -> Dict:
        """
        –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –ø–æ –∑–∞—è–≤–∫–∞–º –Ω–∞ –∏–Ω—Ç–µ—Ä–≤—å—é
        """
        stats = InterviewRequest.objects.aggregate(
            total_requests=Count('id'),
            pending_requests=Count('id', filter=Q(status='pending')),
            approved_requests=Count('id', filter=Q(status='approved')),
            scheduled_requests=Count('id', filter=Q(status='scheduled')),
            completed_requests=Count('id', filter=Q(status='completed')),
            cancelled_requests=Count('id', filter=Q(status='cancelled'))
        )

        return {
            'total_requests': stats['total_requests'] or 0,
            'by_status': {
                'pending': stats['pending_requests'] or 0,
                'approved': stats['approved_requests'] or 0,
                'scheduled': stats['scheduled_requests'] or 0,
                'completed': stats['completed_requests'] or 0,
                'cancelled': stats['cancelled_requests'] or 0
            },
            'success_rate': round(
                (stats['completed_requests'] or 0) / max(stats['total_requests'] or 1, 1) * 100, 1
            )
        }

